/**
 * substr(startIndex, length):
 * The substr() function has the form substr(startIndex,length).
 * It returns the substring from startIndex and returns ‘length’ number of characters.
 */
let s = 'Hello';
console.log(s.substr(1, 4));

/**
 * substring(startIndex, endIndex):
 * The substring() function has the form substring(startIndex,endIndex).
 * It returns the substring from startIndex up to endIndex – 1.
 */
console.log(s.substring(1, 4));

